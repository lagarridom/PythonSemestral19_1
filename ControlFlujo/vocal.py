# Definir si una letra es vocal o no

letra = input("Dame una letra: ")

if letra == "a":
	print("Es la vocal a")
elif letra == "e":
	print("Es la vocal e")
elif letra == "i":
	print("Es la vocal i")
elif letra == "o":
	print("Es la vocal o")
elif letra == "u":
	print("Es la vocal u")
else:
	print("No es una vocal")